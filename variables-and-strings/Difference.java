/* 
Write a program that prints out the following output. 

What is the difference between
a ' and a "?  Or between a " and a \"?

One is what we see when we're typing our program.
The other is what appears on the "console."

Fun pattern is \'"\\" but empty pattern is "".
*/

public class Difference {
    public static void main(String[]args) {
        System.out.print("What is the difference between a \' and a\"?");
        System.out.println(" Or between a \" and \\\"?");
        System.out.println();
        System.out.print("One is what we see when we\'re typing our program.");
        System.out.println(" The other is what appears on the \"console\".");
        System.out.println();
        System.out.println("Fun pattern is \\\'\"\\\\\" but empty pattern is \"\".");
    
    }
}